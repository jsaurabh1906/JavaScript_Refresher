// Math Methods in JS
// Math is a built-in object in JavaScript that provides properties and methods for mathematical constants and functions.

// Math.PI is a property that represents the ratio of the circumference of a circle to its diameter
console.log("Math.PI:", Math.PI); // Output: 3.141592653589793

// abs() method is used to return the absolute value of a number
console.log("Math.abs(-5):", Math.abs(-5)); // Output: 5    

// round() method is used to round a number to the nearest integer
console.log("Math.round(4.7):", Math.round(4.7)); // Output: 5
console.log("Math.round(4.4):", Math.round(4.4)); // Output: 4

// ceil() method is used to round a number up to the nearest integer
console.log("Math.ceil(4.1):", Math.ceil(4.1)); // Output: 5
console.log("Math.ceil(4.9):", Math.ceil(4.9)); // Output: 5

// floor() method is used to round a number down to the nearest integer
console.log("Math.floor(4.1):", Math.floor(4.1)); // Output: 4
console.log("Math.floor(4.9):", Math.floor(4.9)); // Output: 4

// max() method is used to return the largest of zero or more numbers
console.log("Math.max(1, 5, 3):", Math.max(1, 5, 3)); // Output: 5  
console.log("Math.max(-1, -5, -3):", Math.max(-1, -5, -3)); // Output: -1

// min() method is used to return the smallest of zero or more numbers
console.log("Math.min(1, 5, 3):", Math.min(1, 5, 3)); // Output: 1  
console.log("Math.min(-1, -5, -3):", Math.min(-1, -5, -3)); // Output: -5

// random() method is used to return a random number between 0 (inclusive) and 1 (exclusive)
console.log("Math.random():", Math.random()); // Output: a random number between 0 and 1        

// pow() method is used to return the base to the exponent power
console.log("Math.pow(2, 3):", Math.pow(2, 3)); // Output: 8 because it calculates 2 raised to the power of 3  

// sqrt() method is used to return the square root of a number
console.log("Math.sqrt(16):", Math.sqrt(16)); // Output: 4 because it calculates the square root of 16  
console.log("Math.sqrt(22):", Math.sqrt(22)); // Output: 4.69041575982343 because it calculates the square root of 22